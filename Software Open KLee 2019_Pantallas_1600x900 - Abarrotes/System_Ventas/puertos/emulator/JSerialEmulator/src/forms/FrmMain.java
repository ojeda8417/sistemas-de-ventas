    private void performWrite(){
        try {
            if(tmrLecturas == null){
                
                aSettings = util.Util.getAppSettings();
                PortSettings pt = aSettings.getPortSettings();
                int milliSeconds = (int)(Double.parseDouble(pt.getInterval()) * 1000);
                
                puerto = new PortController(pt);
                puerto.preparePort();
                puerto.openPort();
                                
                tmrLecturas = new Timer(milliSeconds, new ActionListener() {
                    public void actionPerformed(ActionEvent ae) {
                        
                        puerto.writeData(txtWeights.getText()+"\r\n");
                    }
                });

                btnTransmit.setText("Detener");
                tmrLecturas.setRepeats(true);
                tmrLecturas.start();
                
            }else{
                
                tmrLecturas.stop();
                tmrLecturas = null;
                
                puerto.closePort();
                puerto = null;

                btnTransmit.setText("Iniciar");                
            }
        } catch (Exception ex) {
         Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
