    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Clientes("");
        GUIMensajeCargaClientes msm = new GUIMensajeCargaClientes();
        msm.setVisible(true);
        try {
            URL jasperUrl = this.getClass().getResource("/Reportes/R_Clientes.jasper");
            JasperReport report = (JasperReport) JRLoader.loadObject(jasperUrl);
            JasperPrint jasperPrint = JasperFillManager.fillReport(report, null, new JMySQL().getConnection());

//            JasperExportManager.exportReportToPdfFile(jasperPrint, "src/pdf/Lista de Clientes.pdf");

            JRViewer jRViewer = new JRViewer(jasperPrint);
            jpContainer.removeAll();
            jpContainer.setSize(5, 5);
            jpContainer.setLayout(new BorderLayout());
            jpContainer.add(jRViewer, BorderLayout.CENTER);
            jRViewer.setVisible(true);
            jpContainer.repaint();
            jpContainer.revalidate();
        } catch (JRException ex) {
            System.err.println(ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

