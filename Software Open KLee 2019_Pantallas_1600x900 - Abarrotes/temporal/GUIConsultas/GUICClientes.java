    public void BuscarClientes() {
        TextAutoCompleter textAutoC = new TextAutoCompleter(txtParameter);
        try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/system_ventas", "root", "");
            Statement sent = con.createStatement();
            ResultSet rs = sent.executeQuery("SELECT nombres FROM tabla_clientes");
            while (rs.next()) {
                textAutoC.addItem(rs.getString("nombres"));

            }
        } catch (SQLException e) {
            System.out.println("No se han registrado clientes");
        }
    }

    void Clientes(String valor) {
        String mostrar = "SELECT * FROM tabla_clientes WHERE CONCAT(codcliente,nombres,identidad) LIKE '%" + valor + "%'";
        String[] titulos = {"Nº", "IDENT.", "NOMBRES", "DIRECCIÓN", "TELÉFONO", "CELULAR", "E-MAIL"};
        String[] Registros = new String[7];
        model = new DefaultTableModel(null, titulos);

        try {
            Statement st = Conexion.conexion().createStatement();
            ResultSet rs = st.executeQuery(mostrar);
            while (rs.next()) {
                Registros[0] = rs.getString("codcliente");
                Registros[1] = rs.getString("identidad");
                Registros[2] = rs.getString("nombres");
                Registros[3] = rs.getString("direccion");
                Registros[4] = rs.getString("telefono");
                Registros[5] = rs.getString("celular");
                Registros[6] = rs.getString("email");

                model.addRow(Registros);
                tb_clientesfact.setAutoResizeMode(tb_clientesfact.AUTO_RESIZE_OFF);
                tb_clientesfact.setRowHeight(18);
                tb_clientesfact.setModel(model);
                lblnota.setText("Existen aproximadamente " + tb_clientesfact.getRowCount() + " clientes registrados en el sistema");
                tb_clientesfact.getColumnModel().getColumn(0).setPreferredWidth(60);
                tb_clientesfact.getColumnModel().getColumn(1).setPreferredWidth(100);
                tb_clientesfact.getColumnModel().getColumn(2).setPreferredWidth(240);
                tb_clientesfact.getColumnModel().getColumn(3).setPreferredWidth(240);
                tb_clientesfact.getColumnModel().getColumn(4).setPreferredWidth(90);
                tb_clientesfact.getColumnModel().getColumn(5).setPreferredWidth(90);
                tb_clientesfact.getColumnModel().getColumn(6).setPreferredWidth(200);

            }

        } catch (SQLException ex) {
            Logger.getLogger(GUICClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

