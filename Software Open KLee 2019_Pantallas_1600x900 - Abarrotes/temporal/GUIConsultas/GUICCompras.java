    void CargartodasFacturas() {
        DefaultTableModel tabla = new DefaultTableModel();
        String[] titulos = {"NUMERO", "PROVEEDOR", "DOCUMENTO", "SUBTOTAL", "IVA %", "TOTAL", "HORA", "FECHA EMISIÓN"};
        tabla.setColumnIdentifiers(titulos);
        this.tabla_compras.setModel(tabla);
        String consulta = "SELECT * FROM tabla_compras";
        String[] Datos = new String[8];
        try {
            Statement st = Conexion.conexion().createStatement();
            ResultSet rs = st.executeQuery(consulta);
            while (rs.next()) {
                Datos[0] = rs.getString("num_comp");
                Datos[1] = rs.getString("proveedor");
                Datos[2] = rs.getString("documento");
                Datos[3] = rs.getString("subtotal");
                Datos[4] = rs.getString("iva");
                Datos[5] = rs.getString("pre_tot");
                Datos[6] = rs.getString("hora");
                Datos[7] = rs.getString("fecha");

                tabla.addRow(Datos);
                tabla_compras.setAutoResizeMode(tabla_compras.AUTO_RESIZE_OFF);
                tabla_compras.setRowHeight(20);
                tabla_compras.getColumnModel().getColumn(0).setPreferredWidth(80);
                tabla_compras.getColumnModel().getColumn(1).setPreferredWidth(300);
                tabla_compras.getColumnModel().getColumn(2).setPreferredWidth(125);
                tabla_compras.getColumnModel().getColumn(3).setPreferredWidth(80);
                tabla_compras.getColumnModel().getColumn(4).setPreferredWidth(80);
                tabla_compras.getColumnModel().getColumn(5).setPreferredWidth(120);
                tabla_compras.getColumnModel().getColumn(6).setPreferredWidth(100);
                tabla_compras.getColumnModel().getColumn(7).setPreferredWidth(100);
                Total();
            }
        } catch (SQLException ex) {
            Logger.getLogger(GUICCompras.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void Total() {

        double sumatoria;
        double sumatoria1 = 0;
        double totalRow = tabla_compras.getRowCount();
        totalRow -= 1;
        for (int i = 0; i <= (totalRow); i++) {
            sumatoria = Double.parseDouble(String.valueOf(tabla_compras.getValueAt(i, 5)));
            sumatoria1 += sumatoria;
        }
        txtcomprast.setText(String.valueOf(sumatoria1));

    }

    public void RedondearTotComp() {
        double valor = Double.parseDouble(GUICCompras.txtcomprast.getText());
        String val = valor + "";
        BigDecimal big = new BigDecimal(val);
        big = big.setScale(2, RoundingMode.HALF_UP);
        System.out.println("Número : " + big);
        txtcomprast.setText("" + big);
    }

