    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        JFileChooser dialog = new JFileChooser();
        int opcion = dialog.showSaveDialog(GUICCompras.this);

        if (opcion == JFileChooser.APPROVE_OPTION) {

            File dir = dialog.getSelectedFile();

            try {
                List<JTable> tb = new ArrayList<JTable>();
                tb.add(tabla_compras);

                Excel excelExporter = new Excel(tb, new File(dir.getAbsolutePath() + ".xls"));
                if (excelExporter.Export()) {

                    JOptionPane.showMessageDialog(null, "Tabla de compras exportada con èxito");
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }    

    }//GEN-LAST:event_jButton1ActionPerformed

