    void Proveedores(String valor) {
        String mostrar = "SELECT * FROM tabla_proveedores WHERE CONCAT(nom_comercial,nombres) LIKE '%" + valor + "%'";
        String[] titulos = {"NÂ° Comercial", "Nombres","E-Mail"};
        String[] Registros = new String[3];
        model = new DefaultTableModel(null, titulos);

        try {
            Statement st = Conexion.conexion().createStatement();
            ResultSet rs = st.executeQuery(mostrar);
            while (rs.next()) {
                Registros[0] = rs.getString("nom_comercial");
                Registros[1] = rs.getString("nombres");
                Registros[2] = rs.getString("email");
                model.addRow(Registros);
            }
            tbcoprov.setModel(model);
            tbcoprov.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            tbcoprov.setRowHeight(20);
            tbcoprov.getColumnModel().getColumn(0).setPreferredWidth(210);
            tbcoprov.getColumnModel().getColumn(1).setPreferredWidth(220);
            tbcoprov.getColumnModel().getColumn(2).setPreferredWidth(250);

        } catch (SQLException ex) {
            Logger.getLogger(GUICCorreoProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

