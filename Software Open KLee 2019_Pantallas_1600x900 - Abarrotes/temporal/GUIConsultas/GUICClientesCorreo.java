    void Clientes(String valor) {
        String mostrar = "SELECT * FROM tabla_clientes WHERE CONCAT(nombres) LIKE '%" + valor + "%'";
        String[] titulos = {"Clientes", "E-Mail"};
        String[] Registros = new String[2];
        model = new DefaultTableModel(null, titulos);

        try {
            Statement st = Conexion.conexion().createStatement();
            ResultSet rs = st.executeQuery(mostrar);
            while (rs.next()) {
                Registros[0] = rs.getString("nombres");
                Registros[1] = rs.getString("email");
                model.addRow(Registros);
            }
            tb_clientcorreo.setModel(model);
            tb_clientcorreo.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            tb_clientcorreo.setRowHeight(20);
            tb_clientcorreo.getColumnModel().getColumn(0).setPreferredWidth(200);
            tb_clientcorreo.getColumnModel().getColumn(1).setPreferredWidth(200);

        } catch (SQLException ex) {
            Logger.getLogger(GUICClientesCorreo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

