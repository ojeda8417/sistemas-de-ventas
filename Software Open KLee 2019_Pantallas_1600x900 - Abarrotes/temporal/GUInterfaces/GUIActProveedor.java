    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            PreparedStatement pst = Conexion.conexion().prepareStatement("UPDATE tabla_proveedores SET tipo='" + cbTipo1.getSelectedItem()
                    + "',documento='" + txtdoc1.getText()
                    + "',nom_comercial='" + txtnomcom1.getText()
                    + "',nombres='" + txtnom1.getText()
                    + "',telefono='" + txttelf1.getText()
                    + "',direccion='" + txtdireccion1.getText()
                    + "',ciudad='" + txtciudad1.getText()
                    + "',email='" + txtemail1.getText() + "' WHERE codprov='" + txtcodprov1.getText() + "'");

            JOptionPane.showMessageDialog(null, "Datos actualizados correctamente");
            pst.executeUpdate();
            txtciudad1.setText("");
            txtdireccion1.setText("");
            txtdoc1.setText("");
            txtemail1.setText("");
            txtnom1.setText("");
            txtnomcom1.setText("");
            txttelf1.setText("");
            cbTipo1.setSelectedItem("[Seleccionar]");

        } catch (Exception e) {
            System.out.print(e.getMessage() + e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

