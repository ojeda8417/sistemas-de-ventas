    void Generarnumeracion() {
        String SQL = "select max(codprov) from tabla_proveedores";

        int c = 0;
        int b = 0;
        try {
            Statement st = Conexion.conexion().createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while (rs.next()) {
                c = rs.getInt(1);
            }

            if (c == 0) {
                txtcodprov1.setText("00001");
            } else {

                txtcodprov1.setText("0000" + (c + 1));

            }
        } catch (SQLException ex) {
            Logger.getLogger(GUIActProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

