    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (this.txtmarca.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan algunos campos", "Correjr", JOptionPane.WARNING_MESSAGE);
            this.txtmarca.requestFocus();
            return;
        }

        String cod,marc;
        String sql = "";
        cod = txtcodmarca.getText();
        marc = txtmarca.getText();       

        sql = "INSERT INTO tabla_marcas (marca) VALUES (?)";
        try {
            PreparedStatement pst = Conexion.conexion().prepareStatement(sql);

            pst.setString(1, marc);
           
            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Marca " + marc + " guardada con éxito");
                Marcas();
                Numeracion();
                txtmarca.setText("");
                GUIProductos.cbMarca.addItem(marc);
            }
        } catch (SQLException ex) {
            Logger.getLogger(GUIMarcas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

