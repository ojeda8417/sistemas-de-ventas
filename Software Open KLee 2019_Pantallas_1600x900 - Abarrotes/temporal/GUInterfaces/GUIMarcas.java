    void Numeracion() {
        String SQL = "select max(codmarca) from tabla_marcas";

        int c = 0;
        int b = 0;
        try {
            Statement st = Conexion.conexion().createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while (rs.next()) {
                c = rs.getInt(1);
            }

            if (c == 0) {
                txtcodmarca.setText("1");
            } else {

                txtcodmarca.setText("" + (c + 1));

            }
        } catch (SQLException ex) {
            Logger.getLogger(GUIMarcas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void Marcas() {
        String[] titulos = {"N°", "MARCA"};
        model = new DefaultTableModel(null, titulos);
        String datos[] = new String[4];
        String sql = "SELECT * FROM  tabla_marcas";
        try {
            Statement st = Conexion.conexion().createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString("codmarca");
                datos[1] = rs.getString("marca");
                model.addRow(datos);
            }
            tb_marcas.setModel(model);
            tb_marcas.setAutoResizeMode(tb_marcas.AUTO_RESIZE_OFF);
            tb_marcas.setRowHeight(20);
            tb_marcas.getColumnModel().getColumn(0).setPreferredWidth(50);
            tb_marcas.getColumnModel().getColumn(1).setPreferredWidth(282);

        } catch (SQLException ex) {
            Logger.getLogger(GUIUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
