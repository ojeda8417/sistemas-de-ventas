    void Usuarios() {
        String[] titulos = {"ID", "USUARIO", "PASS", "TIPO", "PREGUNTA", "RESPUESTA"};
        model = new DefaultTableModel(null, titulos);
        String datos[] = new String[6];
        String sql = "SELECT * FROM  tabla_usuarios";
        try {
            Statement st = Conexion.conexion().createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString("idusuario");
                datos[1] = rs.getString("nick");
                datos[2] = rs.getString("password");
                datos[3] = rs.getString("tipousuario");
                datos[4] = rs.getString("pregunta");
                datos[5] = rs.getString("respuesta");
                model.addRow(datos);
            }
            tabla_usuarios.setModel(model);

            txtcant.setText(" " + tabla_usuarios.getRowCount());

            tabla_usuarios.setAutoResizeMode(tabla_usuarios.AUTO_RESIZE_OFF);
            tabla_usuarios.setRowHeight(15);
            tabla_usuarios.getColumnModel().getColumn(0).setPreferredWidth(40);
            tabla_usuarios.getColumnModel().getColumn(1).setPreferredWidth(300);
            tabla_usuarios.getColumnModel().getColumn(2).setPreferredWidth(150);
            tabla_usuarios.getColumnModel().getColumn(3).setPreferredWidth(150);
            tabla_usuarios.getColumnModel().getColumn(4).setPreferredWidth(150);
            tabla_usuarios.getColumnModel().getColumn(5).setPreferredWidth(150);

        } catch (SQLException ex) {
            Logger.getLogger(GUIUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

