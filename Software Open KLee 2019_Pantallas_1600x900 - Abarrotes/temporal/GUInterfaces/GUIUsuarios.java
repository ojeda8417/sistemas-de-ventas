    private void mnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnmodificarActionPerformed

        int filamodificar = tabla_usuarios.getSelectedRow();
        if (filamodificar >= 0) {
            txtid.setText(tabla_usuarios.getValueAt(filamodificar, 0).toString());
            txtnick.setText(tabla_usuarios.getValueAt(filamodificar, 1).toString());
            txtpass.setText(tabla_usuarios.getValueAt(filamodificar, 2).toString());
            cbTipo.setSelectedItem(tabla_usuarios.getValueAt(filamodificar, 3).toString());
            txtpregunt.setText(tabla_usuarios.getValueAt(filamodificar, 4).toString());
            txtrespuest.setText(tabla_usuarios.getValueAt(filamodificar, 5).toString());

        } else {
            JOptionPane.showMessageDialog(this, "No ha seleccionado");
        }

    }//GEN-LAST:event_mnmodificarActionPerformed

    private void mneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mneliminarActionPerformed

        try {

            int filas = tabla_usuarios.getSelectedRow();

            String desc = tabla_usuarios.getValueAt(filas, 1).toString();

            desc = tabla_usuarios.getValueAt(filas, 0).toString();

            int eliminar = JOptionPane.showConfirmDialog(null, "Desea eliminar este registro?", "Información", JOptionPane.YES_NO_OPTION);
            if (eliminar == 0) {

                PreparedStatement pst = Conexion.conexion().prepareStatement("DELETE FROM tabla_usuarios WHERE idusuario='" + desc + "'");

                JOptionPane.showMessageDialog(this, "Registro eliminado del sistema");
                pst.executeUpdate();
                Usuarios();

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila de la tabla");
        }

    }//GEN-LAST:event_mneliminarActionPerformed


